				<p>Physical therapy (PT), also known as physiotherapy, is one of the allied health professions that, by using mechanical force and movements (bio-mechanics or kinesiology), manual therapy, exercise therapy and electrotherapy, remediates impairments and promotes mobility and function. Physical therapy is used to improve a patient's quality of life through examination, diagnosis, prognosis and physical intervention. It is performed by physical therapists (known as physiotherapists in many countries).

In addition to clinical practice, other activities encompassed in the physical therapy profession include research, education, consultation and administration. Physical therapy services may be provided as primary care treatment or alongside, or in conjunction with, other medical services.</p>
		
		<p>Physical therapy attempts to address the illnesses, or injuries that limit a person's abilities to move and perform functional activities in their daily lives.[1] PTs use an individual's history and physical examination to arrive at a diagnosis and establish a management plan and, when necessary, incorporate the results of laboratory and imaging studies like X-rays, CT-scan, or MRI findings. Electrodiagnostic testing (e.g., electromyograms and nerve conduction velocity testing) may also be used.[2] PT management commonly includes prescription of or assistance with specific exercises, manual therapy and manipulation, mechanical devices such as traction, education, physical agents which includes heat, cold, electricity, sound waves, radiation, assistive devices, prostheses, orthoses and other interventions. In addition, PTs work with individuals to prevent the loss of mobility before it occurs by developing fitness and wellness-oriented programs for healthier and more active lifestyles, providing services to individuals and populations to develop, maintain and restore maximum movement and functional ability throughout the lifespan. This includes providing therapeutic treatment in circumstances where movement and function are threatened by aging, injury, disease or environmental factors. Functional movement is central to what it means to be healthy.

Physical therapy is a professional career which has many specialties including musculoskeletal, sports, neurology, wound care, EMG, cardiopulmonary, geriatrics, orthopedics, women's health, and pediatrics. Neurological rehabilitation is in particular a rapidly emerging field. PTs practice in many settings, such as private-owned physical therapy clinics, outpatient clinics or offices, health and wellness clinics, rehabilitation hospitals facilities, skilled nursing facilities, extended care facilities, private homes, education and research centers, schools, hospices, industrial and this workplaces or other occupational environments, fitness centers and sports training facilities.[3]

Physical therapists also practise in the non-patient care roles such as health policy,[4][5][6][7] health insurance, health care administration and as health care executives.[8][9] Physical therapists are involved in the medical-legal field serving as experts, performing peer review and independent medical examinations.

Education varies greatly by country. The span of education ranges from some countries having little formal education to others having doctoral degrees and post doctoral residencies and fellowships.</p><br>
